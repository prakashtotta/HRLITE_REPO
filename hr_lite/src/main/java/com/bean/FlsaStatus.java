package com.bean;

public class FlsaStatus
{
  public long flsaId;
  public String flsaName;
  public String flsaDesc;
  public String status;
  
  public long getFlsaId()
  {
    return this.flsaId;
  }
  
  public void setFlsaId(long flsaId)
  {
    this.flsaId = flsaId;
  }
  
  public String getFlsaName()
  {
    return this.flsaName;
  }
  
  public void setFlsaName(String flsaName)
  {
    this.flsaName = flsaName;
  }
  
  public String getFlsaDesc()
  {
    return this.flsaDesc;
  }
  
  public void setFlsaDesc(String flsaDesc)
  {
    this.flsaDesc = flsaDesc;
  }
  
  public String getStatus()
  {
    return this.status;
  }
  
  public void setStatus(String status)
  {
    this.status = status;
  }
}
