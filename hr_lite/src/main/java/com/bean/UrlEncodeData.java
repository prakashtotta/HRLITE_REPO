package com.bean;

public class UrlEncodeData
{
  public long urlencodeid;
  public String encodedurl;
  public String url;
  
  public long getUrlencodeid()
  {
    return this.urlencodeid;
  }
  
  public void setUrlencodeid(long urlencodeid)
  {
    this.urlencodeid = urlencodeid;
  }
  
  public String getEncodedurl()
  {
    return this.encodedurl;
  }
  
  public void setEncodedurl(String encodedurl)
  {
    this.encodedurl = encodedurl;
  }
  
  public String getUrl()
  {
    return this.url;
  }
  
  public void setUrl(String url)
  {
    this.url = url;
  }
}
