package com.bean;

public class InterviewState
{
  private int interviewstateid;
  private String interviewstateCode;
  private String interviewstateName;
  
  public int getInterviewstateid()
  {
    return this.interviewstateid;
  }
  
  public void setInterviewstateid(int interviewstateid)
  {
    this.interviewstateid = interviewstateid;
  }
  
  public String getInterviewstateCode()
  {
    return this.interviewstateCode;
  }
  
  public void setInterviewstateCode(String interviewstateCode)
  {
    this.interviewstateCode = interviewstateCode;
  }
  
  public String getInterviewstateName()
  {
    return this.interviewstateName;
  }
  
  public void setInterviewstateName(String interviewstateName)
  {
    this.interviewstateName = interviewstateName;
  }
}
